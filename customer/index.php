  document.addEventListener('DOMContentLoaded', function() {
    var seeMoreButtons = document.querySelectorAll('.see-more-btn');
    seeMoreButtons.forEach(function(button) {
      button.addEventListener('click', function() {
        var card = this.closest('.icon-box');
        var shortDesc = card.querySelector('.description.short');
        var fullDesc = card.querySelector('.description.full');
        if (shortDesc.style.display === 'none') {
          shortDesc.style.display = 'block';
          fullDesc.style.display = 'none';
          this.textContent = 'See More';
        } else {
          shortDesc.style.display = 'none';
          fullDesc.style.display = 'block';
          this.textContent = 'See Less';
        }
      });
    });

    // Initially hide the full descriptions
    var fullDescriptions = document.querySelectorAll('.description.full');
    fullDescriptions.forEach(function(desc) {
      desc.style.display = 'none';
    });
  });
</script>

